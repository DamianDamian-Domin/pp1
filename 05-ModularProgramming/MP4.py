import math

x = 3.7
y = 4

print("Pierwiastek kwadratowy x wynosi:",math.pow(x, 2))
print("x do potęgi y wynosi:",math.pow(x, y))
print("Pierwiastek y-tego stopnia z x wynosi: ",math.pow(x, 1/y))
print("Pole koła o promieniu y wynosi: ",math.pi*math.pow(y, 2))
print("Silnia y wynosi: ",math.factorial(y))
print("Największa możliwa liczba całkowita, mniejsza bądź równa x wynosi: ",math.floor(x))
